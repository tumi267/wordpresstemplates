    <p>We have a multi-faceted approach working across platforms and mediums driven by the belief that community and collaboration is vital to creating a vibrant cultural sector. Our aim is to be a catalyst for creativity by working with a diverse range of practitioners from across regions, countries and continents.</p>
    <!-- /wp:paragraph -->
    <!-- wp:paragraph -->
    <p>Bubblegum Club recently transitioned from a for-profit company to operating as part of the Bubblegum Foundation. This shift reflects our deep commitment to supporting cultural production in a way that prioritises artistic integrity, community, and long-term impact over commercial imperatives.</p>
