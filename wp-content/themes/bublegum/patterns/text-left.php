    <p>Bubblegum Club is a cultural organisation that was established in Johannesburg in 2015 and has been based in Geneva since 2025. Our purpose is to support emerging practitioners and the ideas they champion. We do this primality through our digital publication but also through other activities that include curation, events and exhibitions.</p>
    <!-- /wp:paragraph -->
